public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        double salarioMinimo = 1412.00;
        byte idade = 27;
        short ano = 2024;
        int cep = 123456789; // Se começar com valor 0 talvez seja necessário usar outro tipo.
        long cpf = 19876543210L; // long precisa terminar com L; Se começar com valor 0 talvez seja necessário usar outro tipo.
        float PI = 3.14F; // float precisa terminar com F; variável em MAIÚSCULO ou a palavra "final" antes de qualquer variável lhe tornará uma variável de valor constante.
    }
}
